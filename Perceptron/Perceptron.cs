    class Perceptron
    {
        private List<Neuron> Neurons;

        private List<TrainingSet> TrainSet;

        public Perceptron()
        {
            Neurons = new List<Neuron>();
            TrainSet = new List<TrainingSet>();

            generateNeurons();
        }

        public void train(int phases)
        {
            RandomHolder rand = RandomHolder.GetInstance();
             for(int i=0; i<phases; i++)
            {
                int index = (int)(rand.getRandomDouble() * TrainSet.Count);

                TrainingSet t = TrainSet[index];
            }
        }

        //helper functions
        public void generateNeurons()
        {
            for (int i = 0; i < Constants.NUM_LETTERS; i++)
            {
                Neurons.Add(new Neuron());
            }
        }

        public void addTrainingSet(int[] inputs, int letter)
        {
            TrainSet.Add(new TrainingSet(inputs, letter));
        }

        public double[] getOutputs()
        {
            double[] outputs = new double[Constants.NUM_LETTERS];
            
            for(int i=0; i<Neurons.Count; i++)
            {
                outputs[i] = Neurons[i].getOutput();
            }

            return outputs;
        }

        public void setInputs(int[] inputs)
        {
            foreach(Neuron n in Neurons)
            {
                n.SetSample(inputs);
            }
        }

        public void adjustWeight(double[] outputs)
        {
            for(int i=0; i<Neurons.Count; i++)
            {
                double y = outputs[i] - Neurons[i].getOutput();
                Neurons[i].adjustWeights(y);
            }
        }

        public List<TrainingSet> getTrainingSet()
        {
            return TrainSet;
        }

        public bool generatesLog(string directory)
        {
            return FileManagement.writeLog(directory, Neurons, TrainSet);
        }
    }

    class TrainingSet
    {
        private int[] Inputs;
        private double[] Outputs;
        private int Letter;

        private LetterOutputs expectedOutput;

        public TrainingSet(int[] inputs, int letter)
        {
            Inputs = inputs;

            expectedOutput = LetterOutputs.getInstance();

            Letter = letter;

            Outputs = expectedOutput.getLetter(letter);
        }

        public int[] getInputs()
        {
            return Inputs;
        }

        public double[] getOutputs()
        {
            return Outputs;
        }

        public int getLetter()
        {
            return Letter;
        }
    }

